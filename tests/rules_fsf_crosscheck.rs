/// Two multisets of roles are equal (order-insensitive).
fn role_set_eq(a: &[WideRole], b: &[WideRole]) -> bool {
    let mut a = a.to_vec();
    let mut b = b.to_vec();
    a.sort_unstable();
    b.sort_unstable();
    a == b
}

/// The load-bearing test: every transcribed FSF field agrees with the derived
/// `VariantRules`. A mismatch prints the variant, its `variant.cpp` citation, the
/// field, and (mcr value vs FSF value).
#[test]
fn derived_rules_agree_with_fairy_stockfish_config() {
    for e in table() {
        let r = VariantRef::from_name(e.name)
            .unwrap_or_else(|| panic!("unknown variant {}", e.name))
            .rules();
        let ctx = format!("{} ({})", e.name, e.cite);

        assert_eq!(
            (r.board.width, r.board.height),
            e.board,
            "{ctx}: board (mcr {:?} vs FSF maxFile/maxRank {:?})",
            (r.board.width, r.board.height),
            e.board
        );
        assert_eq!(
            r.castling.enabled, e.castling,
            "{ctx}: castling (mcr {} vs FSF {})",
            r.castling.enabled, e.castling
        );
        if let Some(promo) = e.promo {
            assert!(
                role_set_eq(&r.promotion.roles, promo),
                "{ctx}: promotion set (mcr {:?} vs FSF {:?})",
                r.promotion.roles,
                promo
            );
        }
        if let Some(ranks) = e.double_step {
            assert_eq!(
                r.pawns.double_step_ranks.as_slice(),
                ranks,
                "{ctx}: double-step ranks (mcr {:?} vs FSF {:?})",
                r.pawns.double_step_ranks,
                ranks
            );
        }
        if let Some(ep) = e.en_passant {
            assert_eq!(
                r.pawns.en_passant, ep,
                "{ctx}: en passant (mcr {} vs FSF {})",
                r.pawns.en_passant, ep
            );
        }
        if let Some(sl) = e.stalemate_loss {
            assert_eq!(
                r.terminal.stalemate_is_loss, sl,
                "{ctx}: stalemate_is_loss (mcr {} vs FSF {})",
                r.terminal.stalemate_is_loss, sl
            );
        }
        if let Some(bkl) = e.bare_king_loss {
            assert_eq!(
                r.terminal.bare_king_loss, bkl,
                "{ctx}: bare_king_loss (mcr {} vs FSF {})",
                r.terminal.bare_king_loss, bkl
            );
        }
        if let Some(bkd) = e.bare_king_draw {
            assert_eq!(
                r.terminal.bare_king_draw, bkd,
                "{ctx}: bare_king_draw (mcr {} vs FSF {})",
                r.terminal.bare_king_draw, bkd
            );
        }
        if let Some(wc) = e.wins_on_check {
            assert_eq!(
                r.terminal.wins_on_check, wc,
                "{ctx}: wins_on_check (mcr {} vs FSF {})",
                r.terminal.wins_on_check, wc
            );
        }
        if let Some(royal) = e.royal {
            assert_eq!(
                r.terminal.royal, royal,
                "{ctx}: royal (mcr {:?} vs FSF {:?})",
                r.terminal.royal, royal
            );
        }
    }
}


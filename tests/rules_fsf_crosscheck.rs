/// One transcribed FSF expectation. `None` fields are intentionally not cross-checked
/// (see the module-level notes on skipped mappings).
struct Expect {
    /// mcr name accepted by [`VariantRef::from_name`].
    name: &'static str,
    /// The `variant.cpp` constructor this row is transcribed from.
    cite: &'static str,
    /// `(width, height)` = `(maxFile + 1, maxRank + 1)`. Always checked.
    board: (u8, u8),
    /// FSF `castling`. Always checked.
    castling: bool,
    /// FSF `promotionPieceTypes`, as an mcr role set (order-insensitive).
    promo: Option<&'static [WideRole]>,
    /// White's 0-based double-step ranks from `doubleStep`/`doubleStepRegion`.
    double_step: Option<&'static [u8]>,
    /// Whether `enPassantRegion` leaves en passant available.
    en_passant: Option<bool>,
    /// `stalemateValue == -VALUE_MATE`.
    stalemate_loss: Option<bool>,
    /// FSF `extinction*` describing a bare-king **loss** (`ALL_PIECES`, count 1, `-MATE`).
    bare_king_loss: Option<bool>,
    /// FSF `extinctionValue == VALUE_DRAW` on `ALL_PIECES` (Shatar Robado).
    bare_king_draw: Option<bool>,
    /// FSF `checkCounting` where a single check already wins (Kar Ouk / Check-Shogi).
    wins_on_check: Option<bool>,
    /// The royalty model implied by `extinctionPseudoRoyal` / `dupleCheck` / a
    /// non-royal king.
    royal: Option<RoyalRule>,
}

impl Expect {
    const fn new(name: &'static str, cite: &'static str, board: (u8, u8), castling: bool) -> Self {
        Expect {
            name,
            cite,
            board,
            castling,
            promo: None,
            double_step: None,
            en_passant: None,
            stalemate_loss: None,
            bare_king_loss: None,
            bare_king_draw: None,
            wins_on_check: None,
            royal: None,
        }
    }
    const fn promo(mut self, roles: &'static [WideRole]) -> Self {
        self.promo = Some(roles);
        self
    }
    const fn dstep(mut self, ranks: &'static [u8]) -> Self {
        self.double_step = Some(ranks);
        self
    }
    const fn ep(mut self, v: bool) -> Self {
        self.en_passant = Some(v);
        self
    }
    const fn sloss(mut self, v: bool) -> Self {
        self.stalemate_loss = Some(v);
        self
    }
    const fn bk_loss(mut self, v: bool) -> Self {
        self.bare_king_loss = Some(v);
        self
    }
    const fn bk_draw(mut self, v: bool) -> Self {
        self.bare_king_draw = Some(v);
        self
    }
    const fn win_chk(mut self, v: bool) -> Self {
        self.wins_on_check = Some(v);
        self
    }
    const fn royal(mut self, v: RoyalRule) -> Self {
        self.royal = Some(v);
        self
    }
}

